    private void jButtonRegistrarVentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRegistrarVentaActionPerformed
        String nif_empleado;
        String dni_cliente;
        long id_coche;
        String fecha;
        
        nif_empleado = String.valueOf(jComboBoxNifEmpleadoVenta.getSelectedItem());
        
        if (nuevoCliente == true) {
            dni_cliente = jTextFieldDniNuevoClienteVenta.getText();
            String nombre = jTextFieldNombreNuevoClienteVenta.getText();
            String apellidos = jTextFieldApellidosNuevoClienteVenta.getText();
            c.RegistrarCliente(dni_cliente, nombre, apellidos);
            
            dni_cliente = jTextFieldDniNuevoClienteVenta.getText();
        } else {
            dni_cliente = String.valueOf(jComboBoxDniClienteExistente.getSelectedItem());
        }
        
        id_coche = Long.parseLong(jTextFieldIdCocheConcesionario.getText());
        fecha = c.ObtenerFechaActual();
        
        c.CambiarDisponibilidadAVendido(id_coche);
        c.RegistrarVenta(nif_empleado, dni_cliente, id_coche, fecha);
        
        IniciarTablaVentas();
        c.CargarTablaVentas(dtm);
        
        jTextFieldIdCocheConcesionario.setText("");
        jLabelMarcaCocheVenta.setText("");
        jLabelModeloCocheVenta.setText("");
        jLabelAnyoCocheVenta.setText("");
        jLabelColorCocheVenta.setText("");
        jLabelKilometrajeCocheVenta.setText("");
        jLabelPrecioCocheVenta.setText("");
        
        UsarClienteExistente();
        VolverALista();
        
    }//GEN-LAST:event_jButtonRegistrarVentaActionPerformed

    private void jButtonMostrarDatosCocheActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonMostrarDatosCocheActionPerformed
        String id_String = jTextFieldIdCocheConcesionario.getText();
        long id = 0;

        if (id_String.length() > 0) {
            id = Long.parseLong(id_String);
            IniciarDatosCoche(id);
        } else {
            jLabelError.setText("El campo 'ID' esta vacio.");
        }
    }//GEN-LAST:event_jButtonMostrarDatosCocheActionPerformed

